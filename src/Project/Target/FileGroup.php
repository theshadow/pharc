namespace Pharc\Project\Target;

use Pharc\Project\FileGroupInterface;
use Pharc\Project\FileGroupTrait;

/**
 * Class FileSet
 * @package Pharc\Project\Target
 */
class FileGroup implements FileGroupInterface
{
    use FileGroupTrait;

    /**
     * @var string
     */
    protected $name;

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $name
     * @return static
     */
    public function setName($name)
    {
        $this->name = $name;
        return $this;
    }
}